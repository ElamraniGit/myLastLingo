# Longer words (>=10 chars) that aren't in the lists are likely C1/C2
def _estimate_by_length(word: str) -> Optional[str]:
    """Estimate level based on word length as heuristic."""
    length = len(word)
    if length <= 3:
        return "A1"
    elif length <= 5:
        return "A2"
    elif length <= 7:
        return "B1"
    elif length <= 9:
        return "B2"
    elif length <= 12:
        return "C1"
    else:
        return "C2"


def _estimate_by_complexity(word: str) -> str:
    """Estimate level by letter complexity and patterns."""
    # Count consonant clusters as difficulty indicator
    consonants = "bcdfghjklmnpqrstvwxyz"
    
    # Count consonant clusters of 3+
    clusters = len(re.findall(f'[{consonants}]{3,}', word.lower()))
    
    # Count unusual letter combinations
    unusual = len(re.findall(r'[qxz]', word.lower())) * 2
    unusual += len(re.findall(r'[ph|th|ch|sh|gh]', word.lower()))
    
    # Syllable count approximation
    vowels = "aeiouy"
    syllables = len(re.findall(f'[{vowels}]+', word.lower()))
    
    difficulty_score = clusters + unusual + syllables
    
    if difficulty_score <= 2:
        return "A2"
    elif difficulty_score <= 4:
        return "B1"
    elif difficulty_score <= 6:
        return "B2"
    elif difficulty_score <= 8:
        return "C1"
    else:
        return "C2"


def estimate_level(word: str) -> str:
    """
    Estimate CEFR level for a given English word.
    Returns: A1, A2, B1, B2, C1, or C2
    """
    word_lower = word.lower().strip()
    
    # Check CEFR graded lists
    if word_lower in CEFR_A1:
        return "A1"
    if word_lower in CEFR_A2:
        return "A2"
    if word_lower in CEFR_B1:
        return "B1"
    
    # Check for multi-word expressions
    if ' ' in word_lower:
        return "B2" if len(word_lower) <= 20 else "C1"
    
    # Heuristic estimation
    length_level = _estimate_by_length(word_lower)
    complexity_level = _estimate_by_complexity(word_lower)
    
    # Combine heuristics
    levels = {"A1": 1, "A2": 2, "B1": 3, "B2": 4, "C1": 5, "C2": 6}
    reverse = {1: "A1", 2: "A2", 3: "B1", 4: "B2", 5: "C1", 6: "C2"}
    
    avg_score = (levels.get(length_level, 3) + levels.get(complexity_level, 3)) / 2
    estimated = reverse.get(round(avg_score), "B1")
    
    return estimated